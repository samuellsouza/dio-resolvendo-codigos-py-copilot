# Passo 1: Solicitar duas entradas do usuário
dado1 = input("Digite o primeiro dado: ")
dado2 = input("Digite o segundo dado: ")

# Passo 2: Concatenar os dois dados
resultado = dado1 + " " + dado2  # Adiciona um espaço entre os dois dados

# Passo 3: Exibir o resultado concatenado
print("Resultado da concatenação:", resultado)
# Passo 1: Solicitar uma string e um número inteiro do usuário
texto = input("Digite uma string: ")
quantidade = int(input("Digite um número inteiro: "))

# Passo 2: Repetir a string
resultado = texto * quantidade  # A string será repetida 'quantidade' vezes

# Passo 3: Exibir o resultado
print("Resultado da repetição:\n", resultado)
